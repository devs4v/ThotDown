				<p class="post-content-text">
					This snake is found in Africa in southern Angola and northern Namibia.
					Common names: Angolan python, Anchieta's dwarf python
					Python anchietae is a nonvenomous python species endemic to southern Africa. No subspecies are currently recognized. They may grow up to 183 cm (6 ft). The color pattern is a reddish-brown to brown to almost black ground, overlaid with irregular white or cream colored bands and spots. The belly is yellowish.
				</p>	

	
				
				<h2>
					Python 2: Burmese python
				</h2>
				
				<div class="post-img"><img src = "Python/2.jpg"/></div>
				<p class="post-content-text">
					The Burmese python (Python bivittatus) is one of the five largest snakes in the world, native to a large variation of tropic and subtropic areas of Southern- and Southeast Asia. Until 2009 they were considered a subspecies of Python molurus, but now are recognized as belonging to a distinct species.
					They are often found near water and are sometimes semi-aquatic, but can also be found in trees. Wild individuals average 3.7 metres (12 ft) long,but may reach up to 5.74 metres (19 ft).
				</p>
				
				
				<h2>
					Python 3: Python curtus brongersmai
				</h2>
				
				<div class="post-img"><img src = "Python/3.jpg"/></div>
				<p class="post-content-text">
					Python brongersmai, a subspecies python, is a non-venomous snake found on the Malay Peninsula.Hatchlings range from 10” – 17”, Adult males typically range from 36” (3’) – 60” (5’) in length, and females between 48” (4’) – 72” (6’) although a few 96” (8’) have been recorded. These snakes generally look overweight due to their robust structure. The color pattern consists of rich, bright red to orange to a duller rusty red ground color, although populations with yellow and brown are known. This is overlaid with yellow and tan blotches and stripes that run the length of the body, as well as tan and black spots that extend up the flanks. The belly is white, often with small black markings. The head is usually a shade of grey; individual snakes can change how light and dark the head is.
				</p>
				
				
				<h2>
					Python 4: Python curtus breitensteini
				</h2>
				
				<div class="post-img"><img src = "Python/4.jpg"/></div>
				<p class="post-content-text">
					Python breitensteini is a non-venomous python found mostly on the island of Borneo.Adults have been reported to attain a length of 2.1 m (7 feet), although they are usually no more than 1.2 m (4 feet). Heavy-bodied, they can weigh as much as 13.6 kg (30 lb). Females are generally larger than males. The head is broad with several thermoreceptive pits along the nose. The tail is short and tapering. The color pattern is usually tan with brown blotching, which varies greatly. Though there have been no reported instances of albinism, a few individuals display a significantly lighter color, appearing more yellow than brown. Juveniles have a more contrasting pattern than adults. The head is usually yellow.
				</p>
				
				
				<h2>
					Python 5: Python molurus
				</h2>
				
				<div class="post-img"><img src = "Python/7.jpg"/></div>
				<p class="post-content-text">
					Python molurus is a large nonvenomous python species found in many tropic and subtropic areas of Southern and Southeast Asia. It is known by the common names Indian python, black-tailed python and Indian rock python. The species is limited to Southern Asia. It is generally lighter colored than the Burmese python and reaches usually 3 metres (9.8 ft).
				</br>
					Common names are: Indian python, black-tailed python, Indian rock python, Asian rock python. Referred to as "Ajgar" in Hindi, "Azdaha" in Urdu and "Ojogor" in Bengali. In Sri Lanka the species is commonly referred to as "Pimbura" in Sinhala. The now stemmed off subspecies Python molorus pimbura probably came to light with this common alias found in Sri Lanka.
				</p>
				
				
				<h2>
					Python 6: Python regius
				</h2>
				
				<div class="post-img"><img src = "Python/9.jpg"/></div>
				<p class="post-content-text">
					Python regius is a nonvenomous python species found in Africa. This is the smallest of the African pythons and is popular in the pet trade, largely due to its typically docile temperament. No subspecies are currently recognized. It is also known as royal python or ball python. The name "ball python" refers to the animal's tendency to curl into a ball when stressed or frightened. The name "royal python" (from the Latin regius) comes from the fact that rulers in Africa would wear the python as jewelry.
				</p>
				
				
				<h2>
					Python 7: Python reticulatus
				</h2>
				
				<div class="post-img"><img src = "Python/10.jpg"/></div>
				<p class="post-content-text">
					Python reticulatus, also known as the (Asiatic) reticulated python, is a species of python found in Southeast Asia. Adults can grow to 6.95 m (22.8 ft) in length but normally grow to an average of 3–6 m (10–20 ft). They are the world's longest snakes and longest reptile, but are not the most heavily built. Like all pythons, they are nonvenomous constrictors and normally not considered dangerous to humans. Although large specimens are powerful enough to kill an adult human, attacks are only occasionally reported.
					An excellent swimmer, Python reticulatus has been reported far out at sea and has colonised many small islands within its range. The specific name, reticulatus, is Latin meaning "net-like", or reticulated, and is a reference to the complex color pattern.
				</p>
				
				
				<h2>
					Python 8: Python sebae
				</h2>
				
				<div class="post-img"><img src = "Python/11.jpg"/></div>
				<p class="post-content-text">
					Python sebae, commonly known as the African rock python, is a large, nonvenomous snake of Sub-Saharan Africa. The African rock python is one of seven species in the genus Python. It has two subspecies: one found in Central and Western Africa, the other in Southern Africa. Africa's largest snake and one of the five largest snake species in the world (along with the green anaconda, reticulated python, Burmese python and amethystine python), specimens may approach or exceed 6 m (20 ft). The southern subspecies is generally smaller than its northern relative. The snake is found in a variety of habitats, from forests to near deserts, although usually near sources of water. The African rock python kills its prey by constriction and often eats animals up to the size of antelope, occasionally even crocodiles. The snake reproduces by egg-laying. Unlike most snakes, the female will protect her nest and sometimes even her hatchlings.
				</p>
				
				
