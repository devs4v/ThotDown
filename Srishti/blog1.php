				<div class="post-img"><img src = "Python/12.jpg"/></div>
				<p class="post-content-text">
					Python timoriensis is a nonvenomous python species found in South East Asia. A dwarf form, no subspecies are currently recognized. Timor pythons are a fairly long (7 + feet) but relatively thin python species from the Lesser Sunda Islands in Indonesia. Ironically, there are no known records of Timor Pythons from the Island of Timor. This probably stems from the misidentification of a Macklot's Python (Liasis mackloti) that is similar in appearance and DOES occur on that island. This is a very fast-moving, highly defensive snake that spends a great deal of time in the trees hunting for prey.
